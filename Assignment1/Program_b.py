# For calculating area and circumference of circle

import math  # Importing math module

radius = 7.5  # Radius of circle in cm.

pi = math.pi  # Value of pi

area = pi * radius * radius  # Calculation of area of circle in cm²

print(f"Area of circle is {area} cm².")  # Printing area of circle

circumference = 2 * pi * radius  # Calculation of circumference of circle.

print(f"Circumference of the circle is {circumference} cm.")  # Printing circumference of circle

# To calculate total price of fruits

# Asking for prices of 4 fruits:
price1 = float(input('Price of 1st fruit: '))

price2 = float(input('Price of 2nd fruit: '))

price3 = float(input('Price of 3rd fruit: '))

price4 = float(input('Price of 4th fruit: '))

# Calculating total price of fruits for 2 kg each:
total_price = 2*(price1 + price2 + price3 + price4)

print(f'The total price of fruits is {total_price}') # Printing total price.
